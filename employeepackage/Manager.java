package employeepackage;

public class Manager {
        public String mname;
        public String address;
		public Manager(String mname, String address) {
			super();
			this.mname = mname;
			this.address = address;
		}
		public String getMname() {
			return mname;
		}
		public String getAddress() {
			return address;
		}
        
        
        

	}